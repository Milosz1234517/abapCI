package abapci.coloredProject.colorChanger;

public class TitleIconOverlayRectangle {

	private int width; 
	private int height; 
	public TitleIconOverlayRectangle(int titleIconWidth, int titleIconHeight) {
		width = titleIconWidth; 
		height = titleIconHeight; 
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}

}
